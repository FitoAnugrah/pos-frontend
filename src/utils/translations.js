export const t = (lang, key) => {
  const keys = key.split('.');
  let result = translations[lang] || translations['id'];
  for (const k of keys) {
    if (result && result[k]) {
      result = result[k];
    } else {
      // Fallback to id
      let fb = translations['id'];
      for (const fk of keys) {
        fb = fb ? fb[fk] : null;
      }
      return fb || key;
    }
  }
  return result;
};
